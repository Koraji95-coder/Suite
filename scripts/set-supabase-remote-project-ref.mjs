#!/usr/bin/env node
import {
	getRepoEnvPaths,
	readEnvFile,
	writeEnvEntries,
} from "./lib/env-files.mjs";

const repoRoot = process.cwd();
const { envPath, localEnvPath } = getRepoEnvPaths(repoRoot);
const args = new Set(process.argv.slice(2));

function readArgValue(flag) {
	const index = process.argv.indexOf(flag);
	if (index < 0) return "";
	return String(process.argv[index + 1] || "").trim();
}

function normalizeProjectRef(value) {
	return String(value || "")
		.trim()
		.toLowerCase()
		.replace(/[^a-z0-9_-]/g, "");
}

function extractProjectRefFromUrl(url) {
	const trimmed = String(url || "").trim();
	const match = trimmed.match(/^https:\/\/([a-z0-9-]+)\.supabase\.co(?:\/|$)/i);
	return match ? normalizeProjectRef(match[1]) : "";
}

const explicitProjectRef =
	normalizeProjectRef(readArgValue("--project-ref")) ||
	normalizeProjectRef(readArgValue("--ref"));
const baseEnv = readEnvFile(envPath);
const localEnv = readEnvFile(localEnvPath);
const hostedUrl =
	String(baseEnv.VITE_SUPABASE_URL || "").trim() ||
	String(baseEnv.SUPABASE_URL || "").trim();
const derivedProjectRef = explicitProjectRef || extractProjectRefFromUrl(hostedUrl);

if (!derivedProjectRef) {
	console.error(
		[
			"supabase:remote:target:auto: unable to determine the hosted project ref.",
			"Pass --project-ref <ref>, or make sure .env still contains the hosted Supabase URL.",
		].join("\n"),
	);
	process.exit(1);
}

const nextLocalEnv = {
	...localEnv,
	SUPABASE_REMOTE_PROJECT_REF: derivedProjectRef,
};

writeEnvEntries(
	localEnvPath,
	Object.entries(nextLocalEnv).sort(([left], [right]) =>
		left.localeCompare(right),
	),
	[
		"Machine-local Supabase overrides generated by Suite helper scripts.",
		"Do not commit this file.",
	],
);

const payload = {
	ok: true,
	projectRef: derivedProjectRef,
	source: explicitProjectRef ? "argument" : "env",
	wrote: localEnvPath,
};

if (args.has("--json")) {
	console.log(JSON.stringify(payload, null, 2));
} else {
	console.log(
		`supabase:remote:target:auto: wrote SUPABASE_REMOTE_PROJECT_REF=${derivedProjectRef} to ${localEnvPath}`,
	);
}
